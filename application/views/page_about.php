            <h2 class="font-weight-semibold mb-3">Welcome to Mine Labour Protection Campaign</h2>

            <p class="lead font-weight-normal">Mine Labour Protection Campaign (or MLPC) was established in 1994, following deliberations through a series of workshops, seminars and activities in Jodhpur attended by environmentalists, lawyers and civil society organisations on the mining situation in Rajasthan. These meetings demonstrated the need for a counter voice on the mining situation as well as the necessity for a campaign to protect the rights of mine and quarry workers. The campaign was initially guided by a steering committee comprised of experts from various areas of civil society including Justice V. R. Krishna Iyer, a retired judge of the Supreme Court, as its patron.</p>

            <p>In 1999, after 6 years of operation in the state, an independent review of the campaign strongly recommended to havethe need for an independent secretariat for MLPC to provide the campaign an autonomous entity and to enable the extensive participation of mineworkers.</p>

            <p>On June 7, 2007, MLPC was registered as a not for profit organisation under the Rajasthan Public Trust Act of 1959 at Jodhpur. The organisation was reorganised as the MLPC Trust with the new and targeted slogan: "Regulating mining, empowering workers, developing cooperatives, and sustaining environment."</p>

            <p>Since this time MLPC has been working to improve the mining industry in Rajasthan by working with government, the Human Rights Commission, other NGOs in the sector and the mine workers themselves.</p>

                    <p>The board of MLPC Trust has 5 trustees and 15  executive members representing local not for profit organisations, environmentalists, minority groups, Dalit rights struggle groups, the labour movement, mineworkers collectives, cooperatives and  Self Help Group's.</p>
